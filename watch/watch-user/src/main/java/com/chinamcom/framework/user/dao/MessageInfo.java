package com.chinamcom.framework.user.dao;

import java.util.Date;

public class MessageInfo {
	public Integer id;
	public Integer uid; //用户Id
	public Integer type;//消息类型
	public String title;//消息标题
	public String content;//消息内容
	public Integer status;//消息状态 0：未读 1：已读 2：删除
	private Date createTime;//创建时间
	public String remark;//描述（sos 字符串格式lon,lat;address）
	
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getUid() {
		return uid;
	}
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
}
